#include <iostream>
using namespace std;

int main() {
  float rate = 1.5;
  cout << "Number of millimeters higher in 5 years: " << (rate * 5) << endl;
  cout << "Number of millimeters higher in 7 years: " << (rate * 7) << endl;
  cout << "Number of millimeters higher in 10 years: " << (rate * 10) << endl;
  return 0;
}
